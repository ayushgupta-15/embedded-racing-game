; GET_FONT_BYTE
GET_FONT_BYTE:
    MOVWF TEMP3
    MOVLW HIGH(FONT_TABLE)
    MOVWF PCLATH
    MOVF TEMP3, W
    CALL FONT_TABLE
    CLRF PCLATH
    RETURN

; DRAW_CHAR
DRAW_CHAR:
    MOVF TEMP1, W
    MOVWF TEMP2
    BCF STATUS, 0
    RLF TEMP2, F
    BCF STATUS, 0
    RLF TEMP2, F
    MOVF TEMP1, W
    ADDWF TEMP2, F
    MOVLF 5, LOOP_CNT
DCH_LOOP:
    MOVF TEMP2, W
    CALL GET_FONT_BYTE
    MOVWF DISP_BYTE
    CALL GLCD_PUT_BYTE
    INCF DRAW_X, F
    INCF TEMP2, F
    DECF LOOP_CNT, F
    BTFSS STATUS, 2
    GOTO DCH_LOOP
    MOVLF 0x00, DISP_BYTE
    CALL GLCD_PUT_BYTE
    INCF DRAW_X, F
    RETURN

