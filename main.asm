    CALL DRAW_CHAR
    MOVLF FC_SPC, TEMP1
    CALL DRAW_CHAR
    MOVLF FC_S, TEMP1
    CALL DRAW_CHAR
    MOVLF FC_E, TEMP1
    CALL DRAW_CHAR
    MOVLF FC_L, TEMP1
    CALL DRAW_CHAR
    MOVLF FC_E, TEMP1
    CALL DRAW_CHAR
