; EEPROM READ
EEPROM_READ:
    BANKSEL EEADR
    MOVWF EEADR
    BANKSEL EECON1
    BCF EECON1, 7
    BSF EECON1, 0
    BANKSEL EEDATA
    MOVF EEDATA, W
    BANKSEL GAME_STATE
    RETURN

; EEPROM_WRITE_BYTE
EEPROM_WRITE_BYTE:
    BANKSEL TEMP2
    MOVF TEMP2, W
    BANKSEL EEADR
    MOVWF EEADR
    BANKSEL TEMP1
    MOVF TEMP1, W
    BANKSEL EEDATA
    MOVWF EEDATA
    BANKSEL EECON1
    BCF EECON1, 7
    BSF EECON1, 2
    BCF INTCON, 7
    MOVLW 0x55
    MOVWF EECON2
    MOVLW 0xAA
    MOVWF EECON2
    BSF EECON1, 1
    BSF INTCON, 7
EWB_WAIT:
    BTFSC EECON1, 1
    GOTO EWB_WAIT
    BCF EECON1, 2
    BANKSEL GAME_STATE
    RETURN

; EEPROM_LOAD_HISCORE
EEPROM_LOAD_HISCORE:
    MOVLW EE_SENTINEL
    CALL EEPROM_READ
    MOVWF TEMP1
    MOVLW EE_VALID
    SUBWF TEMP1, W
    BTFSS STATUS, 2
    GOTO ELH_INVALID

    MOVLW EE_HISCORE_LO
    CALL EEPROM_READ
    BANKSEL HISCORE_LO
    MOVWF HISCORE_LO

    MOVLW EE_HISCORE_HI
    CALL EEPROM_READ
    BANKSEL HISCORE_HI
    MOVWF HISCORE_HI
    RETURN

ELH_INVALID:
    BANKSEL HISCORE_LO
    CLRF HISCORE_LO
    CLRF HISCORE_HI
    RETURN

; EEPROM_SAVE_HISCORE
EEPROM_SAVE_HISCORE:
    BANKSEL HISCORE_LO
    MOVFFREG HISCORE_LO, TEMP1
    MOVLF EE_HISCORE_LO, TEMP2
    CALL EEPROM_WRITE_BYTE

    BANKSEL HISCORE_HI
    MOVFFREG HISCORE_HI, TEMP1
    MOVLF EE_HISCORE_HI, TEMP2
    CALL EEPROM_WRITE_BYTE

    MOVLF EE_VALID, TEMP1
    MOVLF EE_SENTINEL, TEMP2
    CALL EEPROM_WRITE_BYTE
    RETURN

; CHECK_AND_SAVE_HISCORE
CHECK_AND_SAVE_HISCORE:
    BANKSEL NEW_BEST
    CLRF NEW_BEST

    BANKSEL SCORE_HI
    MOVF SCORE_HI, W
    BANKSEL HISCORE_HI
    SUBWF HISCORE_HI, W
    BTFSS STATUS, 0
    GOTO CASH_NEW_BEST
    BTFSS STATUS, 2
    RETURN

    BANKSEL SCORE_LO
    MOVF SCORE_LO, W
    BANKSEL HISCORE_LO
    SUBWF HISCORE_LO, W
    BTFSS STATUS, 0
    GOTO CASH_NEW_BEST
    RETURN

CASH_NEW_BEST:
    BANKSEL SCORE_LO
    MOVFFREG SCORE_LO, HISCORE_LO
    MOVFFREG SCORE_HI, HISCORE_HI
    MOVLF 1, NEW_BEST
    RETURN

; SYSTEM_INIT
SYSTEM_INIT:
    BANKSEL GAME_STATE
    MOVLW 0x20
    MOVWF FSR
CLR_LOOP:
    CLRF INDF
    INCF FSR, F
    MOVLW 0x6C
    SUBWF FSR, W
    BTFSS STATUS, 2
    GOTO CLR_LOOP

    CALL GPIO_INIT
    CALL TIMER0_INIT
    BSF INTCON, 7

    CALL LOAD_SPRITES
    CALL EEPROM_LOAD_HISCORE
    CALL GLCD_INIT

    BANKSEL GAME_STATE
    MOVLF STATE_MENU, GAME_STATE

    CALL DRAW_BOOT_SPLASH
    CALL DRAW_MENU
    GOTO MAIN_LOOP

; GAME_RESET
GAME_RESET:
    BANKSEL GAME_STATE
    MOVLF STATE_PLAY, GAME_STATE
    MOVLF FUEL_MAX, FUEL_LEVEL
    MOVLF FUEL_MAX, PREV_FUEL
    MOVLF FUEL_DEC_INT,FUEL_TICK
    MOVLF SPEED_LVL1, SPEED_LEVEL
    MOVLF 1, LEVEL
    MOVLF 1, PLAYER_LANE
    MOVLF 1, PREV_LANE
    CLR16 SCORE_LO, SCORE_HI
    CLRF ROAD_SCROLL
    CLRF GAME_TICK
    CLRF ACTIVE_ENEMIES
    MOVLF SPAWN_LVL1, SPAWN_TIMER
    MOVLF ENEMY_INACTIVE, EN0_LANE
    MOVLF ENEMY_INACTIVE, EN1_LANE
    MOVLF ENEMY_INACTIVE, EN2_LANE
    MOVLF ENEMY_INACTIVE, FUEL_LANE
    CLRF EN0_ROW
    CLRF EN1_ROW
    CLRF EN2_ROW
    MOVLF 0x01, HUD_DIRTY
    CLRF NEW_BEST
    CLRF BLINK_FLAG
    MOVLF 0xAC, LFSR_HI
    MOVLF 0xE1, LFSR_LO

    CALL GLCD_CLEAR_ALL
    CALL DRAW_HUD
    CALL DRAW_ROAD
    CALL DRAW_CAR

    BANKSEL PORTC
    BSF PORTC, 2
    RETURN

; TIMER0_INIT
TIMER0_INIT:
    BANKSEL OPTION_REG
    MOVLW 0b11000111
    MOVWF OPTION_REG
    BANKSEL TMR0
    CLRF TMR0
    MOVLF TMR0_RELOAD, TMR0
    BANKSEL TICK_COUNTER
    CLRF TICK_COUNTER
    CLRF GAME_TICK
    CLRF ISR_FLAGS
    BSF INTCON, 5
    RETURN

; GPIO_INIT
GPIO_INIT:
    BANKSEL ADCON1
    MOVLW 0x06
    MOVWF ADCON1
    BANKSEL TRISA
    MOVLW 0b00000111
    MOVWF TRISA
    BANKSEL TRISB
    MOVLW 0b00000000
    MOVWF TRISB
    BANKSEL TRISC
    MOVLW 0b11111011
    MOVWF TRISC
    BANKSEL TRISD
    MOVLW 0b00000000
    MOVWF TRISD
    BANKSEL PORTA
    CLRF PORTA
    CLRF PORTB
    CLRF PORTC
    CLRF PORTD
    BSF PORTB, GLCD_RST_B
    BANKSEL BTN_STATE
    MOVLF 0x07, BTN_PREV
    MOVLF 0x07, BTN_STATE
    CLRF BTN_EVENT
    RETURN

; BUTTON_SCAN
BUTTON_SCAN:
    BANKSEL PORTA
    MOVF PORTA, W
    ANDLW BTN_MASK_C
    XORLW BTN_MASK_C
    BANKSEL TEMP1
    MOVWF TEMP1
    MOVF BTN_PREV, W
    XORWF TEMP1, W
    MOVWF TEMP2
    MOVF TEMP1, W
    ANDWF TEMP2, W
    MOVWF BTN_EVENT
    MOVFFREG TEMP1, BTN_STATE
    MOVFFREG TEMP1, BTN_PREV
    RETURN

; GLCD DRIVER
GLCD_INIT:
    GLCD_RST_LOW
    MOVLW 1
    CALL DELAY_TICKS
    GLCD_RST_HIGH
    MOVLW 1
    CALL DELAY_TICKS
    GLCD_CS1_ON
    MOVLW GLCD_ON
    CALL GLCD_WRITE_CMD
    MOVLW GLCD_STARTLINE
    CALL GLCD_WRITE_CMD
    MOVLW GLCD_SET_PAGE
    CALL GLCD_WRITE_CMD
    MOVLW GLCD_SET_Y
    CALL GLCD_WRITE_CMD
    GLCD_CS2_ON
    MOVLW GLCD_ON
    CALL GLCD_WRITE_CMD
    MOVLW GLCD_STARTLINE
    CALL GLCD_WRITE_CMD
    MOVLW GLCD_SET_PAGE
    CALL GLCD_WRITE_CMD
    MOVLW GLCD_SET_Y
    CALL GLCD_WRITE_CMD
    CALL GLCD_CLEAR_ALL
    GLCD_CS_NONE
    RETURN

GLCD_WRITE_CMD:
    BANKSEL DISP_TEMP
    MOVWF DISP_TEMP
    GLCD_CMD_MODE
    GLCD_WRITE_MODE
    BANKSEL PORTD
    MOVF DISP_TEMP, W
    MOVWF PORTD
    GLCD_EN_PULSE
    RETURN

GLCD_WRITE_DATA:
    BANKSEL DISP_TEMP
    MOVWF DISP_TEMP
    GLCD_DATA_MODE
    GLCD_WRITE_MODE
    BANKSEL PORTD
    MOVF DISP_TEMP, W
    MOVWF PORTD
    GLCD_EN_PULSE
    RETURN

GLCD_CLEAR_HALF:
    BANKSEL DISP_PAGE
    CLRF DISP_PAGE
CLRH_PAGE:
    BANKSEL DISP_PAGE
    MOVF DISP_PAGE, W
    IORLW GLCD_SET_PAGE
    CALL GLCD_WRITE_CMD
    MOVLW GLCD_SET_Y
    CALL GLCD_WRITE_CMD
    BANKSEL DISP_COL
    CLRF DISP_COL
CLRH_COL:
    MOVLW 0x00
    CALL GLCD_WRITE_DATA
    BANKSEL DISP_COL
    INCF DISP_COL, F
    MOVLW 64
    SUBWF DISP_COL, W
    BTFSS STATUS, 2
    GOTO CLRH_COL
    BANKSEL DISP_PAGE
    INCF DISP_PAGE, F
    MOVLW 8
    SUBWF DISP_PAGE, W
    BTFSS STATUS, 2
    GOTO CLRH_PAGE
    RETURN

GLCD_CLEAR_ALL:
    GLCD_CS1_ON
    CALL GLCD_CLEAR_HALF
    GLCD_CS2_ON
    CALL GLCD_CLEAR_HALF
    GLCD_CS_NONE
    RETURN

GLCD_PUT_BYTE:
    BANKSEL DRAW_X
    MOVLW 64
    SUBWF DRAW_X, W
    BTFSS STATUS, 0
    GOTO GPB_CS1
    GLCD_CS2_ON
    BANKSEL DRAW_X
    MOVLW 64
    SUBWF DRAW_X, W
    GOTO GPB_COL
GPB_CS1:
    GLCD_CS1_ON
    BANKSEL DRAW_X
    MOVF DRAW_X, W
GPB_COL:
    IORLW GLCD_SET_Y
    CALL GLCD_WRITE_CMD
    BANKSEL DRAW_PAGE
    MOVF DRAW_PAGE, W
    IORLW GLCD_SET_PAGE
    CALL GLCD_WRITE_CMD
    BANKSEL DISP_BYTE
    MOVF DISP_BYTE, W
    CALL GLCD_WRITE_DATA
    GLCD_CS_NONE
    RETURN

DRAW_SOLID_ROW:
    BANKSEL DRAW_X
    CLRF DRAW_X
DSR2_LOOP:
    BANKSEL DISP_BYTE
    MOVLF 0xFF, DISP_BYTE
    CALL GLCD_PUT_BYTE
    BANKSEL DRAW_X
    INCF DRAW_X, F
    MOVLW 128
    SUBWF DRAW_X, W
    BTFSS STATUS, 2
    GOTO DSR2_LOOP
    RETURN

DRAW_CLEAR_ROW:
    BANKSEL DRAW_X
    CLRF DRAW_X
DCR_LOOP:
    BANKSEL DISP_BYTE
    MOVLF 0x00, DISP_BYTE
    CALL GLCD_PUT_BYTE
    BANKSEL DRAW_X
    INCF DRAW_X, F
    MOVLW 128
    SUBWF DRAW_X, W
    BTFSS STATUS, 2
    GOTO DCR_LOOP
    RETURN

; DELAY_TICKS
DELAY_TICKS:
    BANKSEL TEMP1
    MOVWF TEMP1
    BANKSEL TICK_COUNTER
    MOVF TICK_COUNTER, W
    BANKSEL TEMP1
    ADDWF TEMP1, F
DLY_WAIT:
    BANKSEL TICK_COUNTER
    MOVF TICK_COUNTER, W
    BANKSEL TEMP1
    SUBWF TEMP1, W
    BTFSS STATUS, 2
    GOTO DLY_WAIT
    BCF ISR_FLAGS, FLAG_TICK
    RETURN

; GET_FONT_BYTE
GET_FONT_BYTE:
    MOVWF TEMP3
    MOVLW HIGH(FONT_TABLE)
    MOVWF PCLATH
    MOVF TEMP3, W
    CALL FONT_TABLE
    CLRF PCLATH
    RETURN

; DRAW_CHAR
DRAW_CHAR:
    MOVF TEMP1, W
    MOVWF TEMP2
    BCF STATUS, 0
    RLF TEMP2, F
    BCF STATUS, 0
    RLF TEMP2, F
    MOVF TEMP1, W
    ADDWF TEMP2, F
    MOVLF 5, LOOP_CNT
DCH_LOOP:
    MOVF TEMP2, W
    CALL GET_FONT_BYTE
    MOVWF DISP_BYTE
    CALL GLCD_PUT_BYTE
    INCF DRAW_X, F
    INCF TEMP2, F
    DECF LOOP_CNT, F
    BTFSS STATUS, 2
    GOTO DCH_LOOP
    MOVLF 0x00, DISP_BYTE
    CALL GLCD_PUT_BYTE
    INCF DRAW_X, F
    RETURN

; SCORE_TO_BCD_3
SCORE_TO_BCD_3:
    CLRF TEMP1
    CLRF TEMP2
    BANKSEL SCORE_LO
    MOVF SCORE_LO, W
    BANKSEL TEMP3
    MOVWF TEMP3
S3H:
    MOVLW 100
    SUBWF TEMP3, W
    BTFSS STATUS, 0
    GOTO S3T
    MOVWF TEMP3
    INCF TEMP1, F
    GOTO S3H
S3T:
    MOVLW 10
    SUBWF TEMP3, W
    BTFSS STATUS, 0
    GOTO S3DONE
    MOVWF TEMP3
    INCF TEMP2, F
    GOTO S3T
S3DONE:
    RETURN

HISCORE_TO_BCD_3:
    CLRF TEMP1
    CLRF TEMP2
    BANKSEL HISCORE_LO
    MOVF HISCORE_LO, W
    BANKSEL TEMP3
    MOVWF TEMP3
    GOTO S3H

DRAW_3DIGITS:
    BANKSEL SCORE_DISP
    MOVF TEMP2, W
    MOVWF SCORE_DISP
    MOVF TEMP3, W
    MOVWF PREV_FUEL
    MOVF TEMP1, W
    ADDLW FC_0
    MOVWF TEMP1
    CALL DRAW_CHAR
    BANKSEL SCORE_DISP
    MOVF SCORE_DISP, W
    ADDLW FC_0
    MOVWF TEMP1
    CALL DRAW_CHAR
    BANKSEL PREV_FUEL
    MOVF PREV_FUEL, W
    ADDLW FC_0
    MOVWF TEMP1
    CALL DRAW_CHAR
    RETURN

