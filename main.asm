    BTFSC STATUS, 2
    GOTO UE_SKIP2
    BANKSEL EN2_ROW
    MOVFFREG EN2_ROW, DRAW_PAGE
    BANKSEL EN2_LANE
    MOVFFREG EN2_LANE, TEMP3
    CALL ERASE_ENEMY
    BANKSEL EN2_ROW
    INCF EN2_ROW, F
    MOVLW CAR_PAGE_TOP
    SUBWF EN2_ROW, W
    BTFSC STATUS, 0
    CALL CHECK_COLLISION
    BANKSEL EN2_ROW
    MOVLW 8
    SUBWF EN2_ROW, W
    BTFSS STATUS, 0
    GOTO UE_DRAW2
    MOVLF ENEMY_INACTIVE, EN2_LANE
    BANKSEL ACTIVE_ENEMIES
    MOVF ACTIVE_ENEMIES, F
    BTFSS STATUS, 2
    DECF ACTIVE_ENEMIES, F
    CALL INCREMENT_SCORE
    GOTO UE_SKIP2
UE_DRAW2:
    BANKSEL EN2_ROW
    MOVFFREG EN2_ROW, DRAW_PAGE
    BANKSEL EN2_LANE
    MOVFFREG EN2_LANE, TEMP3
    CALL DRAW_ENEMY
UE_SKIP2:
    RETURN

; COLLISION CHECKS
CHECK_COLLISION:
    BANKSEL TEMP3
    MOVF TEMP3, W
    BANKSEL PLAYER_LANE
    SUBWF PLAYER_LANE, W
    BTFSS STATUS, 2
    RETURN
    BANKSEL ISR_FLAGS
    BSF ISR_FLAGS, FLAG_COLLISION
    RETURN

; DO_GAME_OVER
DO_GAME_OVER:
    BANKSEL GAME_STATE
    MOVLF STATE_GAMEOVER, GAME_STATE

    CALL CHECK_AND_SAVE_HISCORE
    CALL DRAW_GAMEOVER_SCREEN
    RETURN

; MAIN_LOOP
MAIN_LOOP:
    BANKSEL ISR_FLAGS
    BTFSS ISR_FLAGS, FLAG_TICK
    GOTO MAIN_LOOP
    BCF ISR_FLAGS, FLAG_TICK

    CALL BUTTON_SCAN

    BANKSEL GAME_STATE
    CMP_LIT GAME_STATE, STATE_PLAY
    BTFSS STATUS, 2
    GOTO ML_FSM

    BANKSEL GAME_TICK
    INCF GAME_TICK, F
    BANKSEL SPEED_LEVEL
    MOVF SPEED_LEVEL, W
    BANKSEL GAME_TICK
    SUBWF GAME_TICK, W
    BTFSS STATUS, 2
    GOTO ML_HUD_CHECK

    BANKSEL GAME_TICK
    CLRF GAME_TICK
    BANKSEL ROAD_SCROLL
    INCF ROAD_SCROLL, F
    MOVLW 8
    SUBWF ROAD_SCROLL, W
    BTFSS STATUS, 2
    GOTO ML_SCROLL
    BANKSEL ROAD_SCROLL
    CLRF ROAD_SCROLL

ML_SCROLL:
    BANKSEL FUEL_TICK
    DECF FUEL_TICK, F
    BTFSS STATUS, 2
    GOTO ML_NO_FUEL
    MOVLF FUEL_DEC_INT, FUEL_TICK
    BANKSEL FUEL_LEVEL
    MOVF FUEL_LEVEL, F
    BTFSC STATUS, 2
    GOTO ML_DO_GAME_OVER
    DECF FUEL_LEVEL, F
    MOVLF 1, HUD_DIRTY
ML_NO_FUEL:

    BANKSEL SPAWN_TIMER
    DECF SPAWN_TIMER, F
    BTFSS STATUS, 2
    GOTO ML_NO_SPAWN
    BANKSEL LEVEL
    MOVF LEVEL, W
    MOVWF TEMP1
    CMP_LIT TEMP1, 5
    BTFSC STATUS, 2
    GOTO ML_SP5
    CMP_LIT TEMP1, 4
    BTFSC STATUS, 2
    GOTO ML_SP4
    CMP_LIT TEMP1, 3
    BTFSC STATUS, 2
    GOTO ML_SP3
    CMP_LIT TEMP1, 2
    BTFSC STATUS, 2
    GOTO ML_SP2
    MOVLF SPAWN_LVL1, SPAWN_TIMER
    GOTO ML_DO_SPAWN
ML_SP2: MOVLF SPAWN_LVL2, SPAWN_TIMER
    GOTO ML_DO_SPAWN
ML_SP3: MOVLF SPAWN_LVL3, SPAWN_TIMER
    GOTO ML_DO_SPAWN
ML_SP4: MOVLF SPAWN_LVL4, SPAWN_TIMER
    GOTO ML_DO_SPAWN
ML_SP5: MOVLF SPAWN_LVL5, SPAWN_TIMER
ML_DO_SPAWN:
    CALL SPAWN_ENEMY
ML_NO_SPAWN:

    CALL UPDATE_FUEL_PICKUP
    CALL UPDATE_ENEMIES
    BANKSEL ISR_FLAGS
    BTFSC ISR_FLAGS, FLAG_COLLISION
    GOTO ML_DO_GAME_OVER
    CALL DRAW_ROAD
    CALL DRAW_CAR
    GOTO ML_HUD_CHECK
ML_DO_GAME_OVER:
    BCF ISR_FLAGS, FLAG_COLLISION
    BANKSEL HUD_DIRTY
    CLRF HUD_DIRTY
    CALL DO_GAME_OVER
    GOTO MAIN_LOOP

ML_HUD_CHECK:
    BANKSEL HUD_DIRTY
    MOVF HUD_DIRTY, F
    BTFSS STATUS, 2
    CALL DRAW_HUD

ML_FSM:
    BANKSEL GAME_STATE
    CMP_LIT GAME_STATE, STATE_PLAY
    BTFSC STATUS, 2
    GOTO FSM_PLAY

    CMP_LIT GAME_STATE, STATE_PAUSE
    BTFSC STATUS, 2
    GOTO FSM_PAUSE

    CMP_LIT GAME_STATE, STATE_GAMEOVER
    BTFSC STATUS, 2
    GOTO FSM_GAMEOVER

    CMP_LIT GAME_STATE, STATE_MENU
    BTFSC STATUS, 2
    GOTO FSM_MENU

    MOVLF STATE_MENU, GAME_STATE
    GOTO MAIN_LOOP

; FSM STATES
FSM_PLAY:
    BANKSEL BTN_EVENT
    BTFSS BTN_EVENT, BTN_SEL_B
    GOTO FP_LEFT
    BANKSEL GAME_STATE
    MOVLF STATE_PAUSE, GAME_STATE
    GOTO MAIN_LOOP
FP_LEFT:
    BANKSEL BTN_EVENT
    BTFSS BTN_EVENT, BTN_LEFT_B
    GOTO FP_RIGHT
    BANKSEL PLAYER_LANE
    MOVF PLAYER_LANE, F
    BTFSC STATUS, 2
    GOTO MAIN_LOOP
    CALL ERASE_CAR
    BANKSEL PLAYER_LANE
    DECF PLAYER_LANE, F
    CALL DRAW_CAR
    GOTO MAIN_LOOP
FP_RIGHT:
    BANKSEL BTN_EVENT
    BTFSS BTN_EVENT, BTN_RIGHT_B
    GOTO MAIN_LOOP
    BANKSEL PLAYER_LANE
    CMP_LIT PLAYER_LANE, 2
    BTFSC STATUS, 2
    GOTO MAIN_LOOP
    CALL ERASE_CAR
    BANKSEL PLAYER_LANE
    INCF PLAYER_LANE, F
    CALL DRAW_CAR
    GOTO MAIN_LOOP

FSM_PAUSE:
    BANKSEL BTN_EVENT
    BTFSS BTN_EVENT, BTN_SEL_B
    GOTO MAIN_LOOP
    BANKSEL GAME_STATE
    MOVLF STATE_PLAY, GAME_STATE
    GOTO MAIN_LOOP

FSM_GAMEOVER:
    BANKSEL NEW_BEST
    MOVF NEW_BEST, F
    BTFSC STATUS, 2
    GOTO FGO_BTN
    BANKSEL MENU_FLASH
    INCF MENU_FLASH, F
    MOVLW 39
    SUBWF MENU_FLASH, W
    BTFSS STATUS, 2
    GOTO FGO_BTN
    CLRF MENU_FLASH
    BANKSEL BLINK_FLAG
    INCF BLINK_FLAG, F
    BTFSS BLINK_FLAG, 0
    GOTO FGO_CLEAR_NR
    CALL DRAW_NEW_RECORD
    GOTO FGO_BTN
FGO_CLEAR_NR:
    MOVLF 4, DRAW_PAGE
    CALL DRAW_CLEAR_ROW

FGO_BTN:
    BANKSEL BTN_STATE
    BTFSS BTN_STATE, BTN_SEL_B
    GOTO MAIN_LOOP
    BANKSEL GAME_STATE
    MOVLF STATE_MENU, GAME_STATE
    CALL DRAW_MENU
    GOTO MAIN_LOOP  

FSM_MENU:
    BANKSEL MENU_FLASH
    INCF MENU_FLASH, F
    MOVLW 12
    SUBWF MENU_FLASH, W
    BTFSS STATUS, 2
    GOTO FM_BTN
    CLRF MENU_FLASH
    BANKSEL BLINK_FLAG
    INCF BLINK_FLAG, F
    BTFSS BLINK_FLAG, 0
    GOTO FM_FLASH_CLEAR
    CALL DRAW_MENU_CURSOR
    GOTO FM_BTN
FM_FLASH_CLEAR:
    MOVLF 6, DRAW_PAGE
    MOVLF 46, DRAW_X
    MOVLF FC_SPC, TEMP1
    CALL DRAW_CHAR

FM_BTN:
    BANKSEL BTN_EVENT
    BTFSS BTN_EVENT, BTN_SEL_B
    GOTO MAIN_LOOP
    CALL GAME_RESET
    GOTO MAIN_LOOP

